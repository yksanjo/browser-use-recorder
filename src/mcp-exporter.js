// Browser Use Recorder - MCP Server Exporter
// Exports recorded interactions as a reusable MCP server

const { CodeGenerator } = require('./code-generator');

class MCPExporter {
  constructor(events, options = {}) {
    this.events = events;
    this.serverName = options.serverName || 'browser-automation';
    this.description = options.description || 'Auto-generated browser automation MCP server';
    this.toolName = options.toolName || 'run_browser_automation';
    this.framework = options.framework || 'playwright';
    this.language = options.language || 'javascript';
  }

  export() {
    const files = {};

    // Generate the automation code
    const generator = new CodeGenerator(this.events, {
      framework: this.framework,
      language: 'javascript', // MCP server is always JS
      includeComments: true,
      headless: true,
      testName: 'runAutomation',
    });

    const automationCode = generator.generate();

    // Package.json for the MCP server
    files['package.json'] = JSON.stringify({
      name: this.serverName,
      version: '1.0.0',
      description: this.description,
      main: 'index.js',
      scripts: {
        start: 'node index.js',
      },
      dependencies: {
        playwright: '^1.40.0',
      },
    }, null, 2);

    // Main MCP server file
    files['index.js'] = this._generateMCPServer(automationCode);

    // The raw automation script
    files['automation.js'] = automationCode;

    // README
    files['README.md'] = this._generateReadme();

    // Recording data
    files['recording.json'] = JSON.stringify({
      events: this.events,
      metadata: {
        exportedAt: new Date().toISOString(),
        eventCount: this.events.length,
        framework: this.framework,
        urls: [...new Set(this.events.filter(e => e.url).map(e => e.url))],
        hasDecisionPoints: this.events.some(e => e.type === 'ai_decision'),
      },
    }, null, 2);

    return { files };
  }

  _generateMCPServer(automationCode) {
    // Extract unique URLs and actions for tool description
    const urls = [...new Set(this.events.filter(e => e.url).map(e => e.url))];
    const actionTypes = [...new Set(this.events.map(e => e.type))];
    const hasDecisions = this.events.some(e => e.type === 'ai_decision');
    const decisions = this.events.filter(e => e.type === 'ai_decision');

    return `#!/usr/bin/env node
// Auto-generated MCP Server - ${this.serverName}
// Generated by Browser Use Recorder
// ${new Date().toISOString()}

const { chromium } = require('playwright');

// ─── MCP Protocol Implementation ────────────────────────────────────

const SERVER_INFO = {
  name: '${this.serverName}',
  version: '1.0.0',
  description: '${this.description}',
};

const TOOLS = [
  {
    name: '${this.toolName}',
    description: 'Run the recorded browser automation sequence. ${urls.length > 0 ? 'Visits: ' + urls[0] : ''}. Actions: ${actionTypes.join(', ')}${hasDecisions ? '. Includes AI decision points.' : ''}',
    inputSchema: {
      type: 'object',
      properties: {
        headless: {
          type: 'boolean',
          description: 'Run browser in headless mode (default: true)',
          default: true,
        },
        baseUrl: {
          type: 'string',
          description: 'Override the base URL for the automation',
        },
        variables: {
          type: 'object',
          description: 'Variables to inject into the automation (e.g., form values)',
          additionalProperties: { type: 'string' },
        },
        timeout: {
          type: 'number',
          description: 'Maximum execution time in milliseconds (default: 30000)',
          default: 30000,
        },
      },
    },
  },
  {
    name: 'get_recording_info',
    description: 'Get information about the recorded browser automation',
    inputSchema: {
      type: 'object',
      properties: {},
    },
  },
${hasDecisions ? `  {
    name: 'get_decision_points',
    description: 'Get all AI decision points in the automation',
    inputSchema: {
      type: 'object',
      properties: {},
    },
  },` : ''}
];

const RESOURCES = [
  {
    uri: 'recording://events',
    name: 'Recording Events',
    description: 'Raw recorded browser events',
    mimeType: 'application/json',
  },
  {
    uri: 'recording://code',
    name: 'Generated Code',
    description: 'Generated automation code',
    mimeType: 'text/javascript',
  },
];

// ─── Automation Code ────────────────────────────────────────────────

const AUTOMATION_CODE = ${JSON.stringify(automationCode)};

const RECORDING_EVENTS = ${JSON.stringify(this.events)};

// ─── Run Automation ─────────────────────────────────────────────────

async function runAutomation(params = {}) {
  const headless = params.headless !== false;
  const timeout = params.timeout || 30000;
  const variables = params.variables || {};
  const results = [];
  
  const browser = await chromium.launch({ headless });
  const context = await browser.newContext({
    viewport: { width: 1280, height: 720 },
  });
  const page = await context.newPage();

  try {
    // Set timeout
    page.setDefaultTimeout(timeout);

    // Execute recorded steps
${this._generateInlineSteps('    ')}

    results.push({ status: 'completed', stepsExecuted: results.length });
  } catch (error) {
    results.push({ status: 'error', error: error.message, stepsExecuted: results.length });
  } finally {
    await browser.close();
  }

  return results;
}

// ─── MCP Message Handler ────────────────────────────────────────────

async function handleMessage(message) {
  const { method, params, id } = message;

  switch (method) {
    case 'initialize':
      return {
        jsonrpc: '2.0',
        id,
        result: {
          protocolVersion: '2024-11-05',
          capabilities: {
            tools: {},
            resources: {},
          },
          serverInfo: SERVER_INFO,
        },
      };

    case 'tools/list':
      return {
        jsonrpc: '2.0',
        id,
        result: { tools: TOOLS },
      };

    case 'tools/call': {
      const { name, arguments: args } = params;
      
      if (name === '${this.toolName}') {
        try {
          const results = await runAutomation(args || {});
          return {
            jsonrpc: '2.0',
            id,
            result: {
              content: [{
                type: 'text',
                text: JSON.stringify(results, null, 2),
              }],
            },
          };
        } catch (error) {
          return {
            jsonrpc: '2.0',
            id,
            result: {
              content: [{
                type: 'text',
                text: JSON.stringify({ error: error.message }),
              }],
              isError: true,
            },
          };
        }
      }

      if (name === 'get_recording_info') {
        const info = {
          eventCount: RECORDING_EVENTS.length,
          urls: [...new Set(RECORDING_EVENTS.filter(e => e.url).map(e => e.url))],
          actionTypes: [...new Set(RECORDING_EVENTS.map(e => e.type))],
          hasDecisionPoints: RECORDING_EVENTS.some(e => e.type === 'ai_decision'),
          decisionCount: RECORDING_EVENTS.filter(e => e.type === 'ai_decision').length,
        };
        return {
          jsonrpc: '2.0',
          id,
          result: {
            content: [{ type: 'text', text: JSON.stringify(info, null, 2) }],
          },
        };
      }

${hasDecisions ? `      if (name === 'get_decision_points') {
        const decisions = RECORDING_EVENTS
          .filter(e => e.type === 'ai_decision')
          .map(e => ({
            condition: e.condition,
            ifTrue: e.ifTrue,
            ifFalse: e.ifFalse,
            targetSelector: e.targetSelector,
            description: e.description,
          }));
        return {
          jsonrpc: '2.0',
          id,
          result: {
            content: [{ type: 'text', text: JSON.stringify(decisions, null, 2) }],
          },
        };
      }` : ''}

      return {
        jsonrpc: '2.0',
        id,
        error: { code: -32601, message: 'Tool not found: ' + name },
      };
    }

    case 'resources/list':
      return {
        jsonrpc: '2.0',
        id,
        result: { resources: RESOURCES },
      };

    case 'resources/read': {
      const { uri } = params;
      if (uri === 'recording://events') {
        return {
          jsonrpc: '2.0',
          id,
          result: {
            contents: [{
              uri,
              mimeType: 'application/json',
              text: JSON.stringify(RECORDING_EVENTS, null, 2),
            }],
          },
        };
      }
      if (uri === 'recording://code') {
        return {
          jsonrpc: '2.0',
          id,
          result: {
            contents: [{
              uri,
              mimeType: 'text/javascript',
              text: AUTOMATION_CODE,
            }],
          },
        };
      }
      return {
        jsonrpc: '2.0',
        id,
        error: { code: -32602, message: 'Resource not found: ' + uri },
      };
    }

    default:
      return {
        jsonrpc: '2.0',
        id,
        error: { code: -32601, message: 'Method not found: ' + method },
      };
  }
}

// ─── stdio Transport ────────────────────────────────────────────────

let buffer = '';

process.stdin.setEncoding('utf8');
process.stdin.on('data', async (chunk) => {
  buffer += chunk;
  
  const lines = buffer.split('\\n');
  buffer = lines.pop() || '';
  
  for (const line of lines) {
    if (!line.trim()) continue;
    try {
      const message = JSON.parse(line);
      const response = await handleMessage(message);
      if (response) {
        process.stdout.write(JSON.stringify(response) + '\\n');
      }
    } catch (e) {
      process.stderr.write('Error: ' + e.message + '\\n');
    }
  }
});

process.stderr.write('${this.serverName} MCP server started\\n');
`;
  }

  _generateInlineSteps(indent) {
    const lines = [];
    let firstUrl = null;

    for (const event of this.events) {
      if (!firstUrl && event.url) {
        firstUrl = event.url;
        lines.push(`${indent}await page.goto(variables.baseUrl || '${firstUrl}');`);
        lines.push(`${indent}results.push({ step: 'navigate', url: variables.baseUrl || '${firstUrl}' });`);
        continue;
      }

      switch (event.type) {
        case 'click':
          if (event.element?.selector) {
            lines.push(`${indent}await page.click('${this._esc(event.element.selector)}');`);
            lines.push(`${indent}results.push({ step: 'click', selector: '${this._esc(event.element.selector)}' });`);
          }
          break;
        case 'input':
          if (event.element?.selector) {
            const varKey = event.element.name || event.element.id || 'input';
            lines.push(`${indent}await page.fill('${this._esc(event.element.selector)}', variables['${varKey}'] || '${this._esc(event.inputValue)}');`);
            lines.push(`${indent}results.push({ step: 'fill', selector: '${this._esc(event.element.selector)}' });`);
          }
          break;
        case 'select':
          if (event.element?.selector) {
            lines.push(`${indent}await page.selectOption('${this._esc(event.element.selector)}', '${this._esc(event.selectedValue)}');`);
            lines.push(`${indent}results.push({ step: 'select', value: '${this._esc(event.selectedValue)}' });`);
          }
          break;
        case 'keydown':
          lines.push(`${indent}await page.keyboard.press('${event.key}');`);
          lines.push(`${indent}results.push({ step: 'keypress', key: '${event.key}' });`);
          break;
        case 'navigation':
          if (event.url && event.url !== firstUrl) {
            lines.push(`${indent}await page.goto('${this._esc(event.url)}');`);
            lines.push(`${indent}results.push({ step: 'navigate', url: '${this._esc(event.url)}' });`);
          }
          break;
        case 'ai_decision':
          lines.push(`${indent}// 🤖 AI Decision: ${event.description || event.condition}`);
          lines.push(`${indent}{`);
          if (event.targetSelector) {
            lines.push(`${indent}  const el = await page.$('${this._esc(event.targetSelector)}');`);
            lines.push(`${indent}  const text = el ? await el.textContent() : '';`);
          } else {
            lines.push(`${indent}  const text = await page.textContent('body');`);
          }
          lines.push(`${indent}  const num = parseFloat((text || '').replace(/[^0-9.]/g, ''));`);
          lines.push(`${indent}  // Condition: ${event.condition}`);
          lines.push(`${indent}  const met = !isNaN(num) ? eval('num ${event.condition.replace(/'/g, "\\'")}') : text.includes('${this._esc(event.condition)}');`);
          lines.push(`${indent}  results.push({ step: 'decision', condition: '${this._esc(event.condition)}', met });`);
          lines.push(`${indent}}`);
          break;
      }
    }

    return lines.join('\n');
  }

  _generateReadme() {
    const urls = [...new Set(this.events.filter(e => e.url).map(e => e.url))];
    const decisions = this.events.filter(e => e.type === 'ai_decision');

    return `# ${this.serverName}

Auto-generated MCP server from Browser Use Recorder.

## Overview

- **Events recorded:** ${this.events.length}
- **URLs visited:** ${urls.length}
- **AI Decision Points:** ${decisions.length}
- **Framework:** ${this.framework}

## URLs

${urls.map(u => `- ${u}`).join('\n')}

${decisions.length > 0 ? `## AI Decision Points

${decisions.map(d => `- **${d.description || d.condition}**: if \`${d.condition}\` → ${d.ifTrue} / else → ${d.ifFalse}`).join('\n')}
` : ''}

## Setup

\`\`\`bash
npm install
\`\`\`

## Usage as MCP Server

Add to your MCP settings:

\`\`\`json
{
  "mcpServers": {
    "${this.serverName}": {
      "command": "node",
      "args": ["${this.serverName}/index.js"]
    }
  }
}
\`\`\`

## Tools

### \`${this.toolName}\`
Run the recorded browser automation sequence.

**Parameters:**
- \`headless\` (boolean): Run in headless mode (default: true)
- \`baseUrl\` (string): Override the starting URL
- \`variables\` (object): Override form input values
- \`timeout\` (number): Max execution time in ms

### \`get_recording_info\`
Get metadata about the recording.

## Resources

- \`recording://events\` - Raw recorded events (JSON)
- \`recording://code\` - Generated automation code (JS)

## Standalone Usage

\`\`\`bash
node automation.js
\`\`\`
`;
  }

  _esc(str) {
    if (!str) return '';
    return str.replace(/\\/g, '\\\\').replace(/'/g, "\\'").replace(/"/g, '\\"');
  }
}

module.exports = { MCPExporter };
